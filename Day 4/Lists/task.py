# 미국 주 이름 리스트 생성
state_of_america = ["Delaware", "Pennsylvania"]

# 리스트의 첫 번째 항목 출력 (인덱스 0)
print(state_of_america[0])

# 리스트 확장: ["Seoul", "Busan"]을 추가
state_of_america.extend(["Seoul", "Busan"])
print(state_of_america)

# 리스트 끝에 "Daegu"를 추가
state_of_america.append("Daegu")
print(state_of_america)
